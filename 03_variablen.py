
# Wir können einer Zahl einen Namen geben, mit dem Gleichheitszeichen
# Achtung: der Name muss immer links stehen

# Funktioniert nicht:
#42 = r

# Funktioniert
r = 42

# Danach können wir den Namen anstelle des Wertes verwenden
print(r)

# Achtung, Variablen müssen zuerst erstellt werden, bevor sie verwendet werden können

# Error:
#print(x)

x = 5
print(x * x)

# Zuerst wird die Berechnung auf der rechten Seite gemacht, erst danach das Resultat
# unter einem Namen abgespeichert.
y = (5 * 7) + 2
print(y)

# Theoretische Alternativen
# x := 5 + 4
# 5 + 4 -> x

# Die Namen von Variablen können beliebig lang sein
eine_sehr_sehr_gute_zahl_um_damit_zu_rechnen = 1
print(eine_sehr_sehr_gute_zahl_um_damit_zu_rechnen)


